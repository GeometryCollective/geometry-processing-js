/**
 * Clamps a number between the specified minimum and maximum values.
 * @global
 * @function module:Utils.clamp
 * @param {number} x The number to be clamped.
 * @param {number} min The minimum value of x.
 * @param {number} max The maximum value of x.
 * @returns {number}
 */
function clamp(x, min, max) {
	return Math.max(Math.min(x, max), min);
}

/**
 * Maps a number to RGB color values.
 * @global
 * @function module:Utils.colormap
 * @param {number} x The number to be mapped to RGB color values.
 * @param {number} min The minimum value of x.
 * @param {number} max The maximum value of x.
 * @param {Array.<Array.<number>>} values The colormap. The current available options
 * are {@link https://matplotlib.org/examples/color/colormaps_reference.html seismic, coolwarm and hot}.
 * @returns {module:LinearAlgebra.Vector}
 */
function colormap(x, min, max, values) {
	x = Math.max(Math.min(x, max), min);
	x = (x - min) / (max - min);

	let i = 1;
	while (values[i][0] < x) i = i + 1;
	i = i - 1;

	let c1 = new Vector(values[i][1][0], values[i][1][1], values[i][1][2]);
	let c2 = new Vector(values[i + 1][1][0], values[i + 1][1][1], values[i + 1][1][2]);
	let scaling = (x - values[i][0]) / Math.abs(values[i][0] - values[i + 1][0]);

	return c1.plus(c2.minus(c1).times(scaling));
}
